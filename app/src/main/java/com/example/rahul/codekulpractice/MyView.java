public class MyView {

    private String Name;
    private float Price;
    private int Cashback;

    public float getPrice() {
        return Price;
    }

    public void setPrice(float price) {
        Price = price;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getCashback() {
        return Cashback;
    }

    public void setCashback(int cashback) {
        Cashback = cashback;
    }

}